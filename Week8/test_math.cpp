#include <iostream>
#include <cmath>

using namespace std;

int main() {
    cout << "sqrt(4)=" << sqrt(4) << endl;
    cout << "cbrt(8)="<<cbrt(8) <<endl;
    cout << "cbrt(-8)="<<cbrt(-8) <<endl;
    cout << "e^2=" << exp(2) << endl;
    cout << "log4=" << log(4) << endl; // natural logarithm, base e
    cout << "log8=" << log(8) << endl;
    cout << "log10(100)=" << log10(100) << endl;
    cout << "2^4=" << pow(2, 4) << endl;
    return 0;
}